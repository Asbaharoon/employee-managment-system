    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = table.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {

                    Punetori p = ptm.getPersoni(selectedIndex);
                    ArrayList<Telefoni> telefonNumbers = new ArrayList<>(p.getTelefoniCollection());
                    punetoriIdTxt.setText(p.getPunetoriID().toString());
                    emriTxt.setText(p.getEmri());
                    mbiemriTxt.setText(p.getMbiemri());

                    pozitaCB.repaint();

                    if (p.getGjinia() == "M") {
                        mashkullRadio.setSelected(true);
                        femerRadio.setSelected(false);

                    } else if (p.getGjinia() == "F") {
                        femerRadio.setSelected(true);
                        mashkullRadio.setSelected(false);
                    }
                    dcb.setSelectedItem(p.getDepartmentiId());
                    departmentiCB.repaint();
                    emailTxt.setText(p.getEmail());
                    tcbm.setSelectedItem(p.getTitulli());
                    pozitaCB.repaint();
                    adresaTxt.setText(p.getAdresa());
                    qytetiTxt.setText(p.getQyteti());
                    datapunesimit.setDate(p.getDataPunesimit());
                    datalindjes.setDate(p.getDataLindjes());

                    int roli = p.getPerdoruesi().getRoliID().getRoliID();
                    if (roli == 2) {
                        roliCB.setSelectedIndex(0);
                    } else if (roli == 3) {
                        roliCB.setSelectedIndex(1);
                    }
                    if (telefonNumbers.size() >= 1) {
                        Telefoni t = telefonNumbers.get(0);
                        homeTxt.setText(t.getTelefoniShtepise());
                        biznisTelTxt.setText(t.getTelefoniBiznesit());
                        telMobilTxt.setText(t.getTelefoniMobil());
                    } else {
                        homeTxt.setText("");
                        biznisTelTxt.setText("");
                        telMobilTxt.setText("");
                    }

                }
            }
        });
    }

    public void loadTable() {
        try {
            List<Punetori> lista = pr.findOnlyEmployees();

            List<Telefoni> lista_tel = tr.findAll();
            ptm.addList(lista, lista_tel);
            table.setModel(ptm);
            ptm.fireTableDataChanged();
        } catch (EmployeeException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

