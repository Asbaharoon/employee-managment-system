    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            int row = table.getSelectedRow();
            if (row == -1) { //nuk kemi selektuar asgje
                if (CheckMismatchType() && fieldsValidation()) {

                    Punetori p = new Punetori();
                    Telefoni t = new Telefoni();
                    Perdoruesi perdoruesi = new Perdoruesi();
                    p.setEmri(emriTxt.getText());
                    p.setMbiemri(mbiemriTxt.getText());
                    p.setDataLindjes(datalindjes.getDate());
                    p.setQyteti(qytetiTxt.getText());
                    p.setDataPunesimit(datapunesimit.getDate());
                    p.setAdresa(adresaTxt.getText());
                    p.setEmail(emailTxt.getText());
                    p.setDepartmentiId((Departmenti) departmentiCB.getSelectedItem());
                    t.setTelefoniShtepise(homeTxt.getText());
                    t.setTelefoniBiznesit(biznisTelTxt.getText());
                    t.setTelefoniMobil(telMobilTxt.getText());
                    t.setPunetoriId(p);
                    p.setTitulli((TitulliPunetorit) pozitaCB.getSelectedItem());

                    // Create list and add telefoni
                    List<Telefoni> numrat = new ArrayList<>();
                    numrat.add(t);
                    p.setTelefoniCollection(numrat);
                    t.setPunetoriId(p);
                    if (mashkullRadio.isSelected()) {
                        p.setGjinia("M");
                        mashkullRadio.setSelected(true);
                        femerRadio.setSelected(false);
                    } else if (femerRadio.isSelected()) {
                        p.setGjinia("F");
                        mashkullRadio.setSelected(false);
                        femerRadio.setSelected(true);
                    }
                    Roli r = new Roli();
                    String roli = roliCB.getSelectedItem().toString();
                    if (roli.equals("Punetor")) {
                        r.setRoliID(2);
                    } else if (roli.equals("Menaxher")) {
                        r.setRoliID(3);
                    }
                    perdoruesi.setUsername(usernameTxt.getText());
                    perdoruesi.setPassword(new String(passTxt.getPassword()));
                    perdoruesi.setPunetoriID(p);
                    perdoruesi.setRoliID(r);
                    //pr.create(p);
                    perdoruesiRepository.create(perdoruesi);
                    clear();
                } else {
                    JOptionPane.showMessageDialog(null, "Ju lutem kontrolloni fieldat,shenoni parametrat e sakt , dhe mos leni fielda te pa plotesuar");
                }
            } else {

                //EDIT
                usernameTxt.setEditable(false);
                passTxt.setEditable(false);

                if (CheckMismatchTypeEdit() && fieldsValidationEdit()) {
                    Punetori p = ptm.getPersoni(row);
                    ArrayList<Telefoni> telefonNumbers = new ArrayList<>(p.getTelefoniCollection());
                    p.setEmri(emriTxt.getText());
                    p.setMbiemri(mbiemriTxt.getText());
                    p.setDataLindjes(datalindjes.getDate());
                    p.setDataPunesimit(datapunesimit.getDate());
                    p.setEmail(emailTxt.getText());
                    p.setQyteti(qytetiTxt.getText());
                    p.setAdresa(adresaTxt.getText());
                    p.setDepartmentiId((Departmenti) departmentiCB.getSelectedItem());
                    p.setTitulli((TitulliPunetorit) pozitaCB.getSelectedItem());
                    if (mashkullRadio.isSelected()) {
                        p.setGjinia("M");
                    } else if (femerRadio.isSelected()) {
                        p.setGjinia("F");
                    }
                    Telefoni t = null;
                    if (!telefonNumbers.isEmpty()) {
                        t = telefonNumbers.get(0);
                        t.setTelefoniShtepise(homeTxt.getText());
                        t.setTelefoniBiznesit(biznisTelTxt.getText());
                        t.setTelefoniMobil(telMobilTxt.getText());
                    } else {
                        if (!homeTxt.getText().isEmpty()
                                || !biznisTelTxt.getText().isEmpty() || !telMobilTxt.getText().isEmpty()) {
                            System.out.println("New Telefoni object created");
                            t = new Telefoni();
                            t.setTelefoniShtepise(homeTxt.getText());
                            t.setTelefoniBiznesit(biznisTelTxt.getText());
                            t.setTelefoniMobil(telMobilTxt.getText());
                        }
                    }
                    // Create list and add telefoni
                    if (t != null) {
                        List<Telefoni> numrat = new ArrayList<>();
                        numrat.add(t);
                        p.setTelefoniCollection(numrat);
                        t.setPunetoriId(p);
                    }
                    if (mashkullRadio.isSelected()) {
                        p.setGjinia("M");
                        femerRadio.setSelected(false);
                    } else if (femerRadio.isSelected()) {
                        p.setGjinia("F");
                        mashkullRadio.setSelected(false);
                    }
                    pr.edit(p);
//                tr.edit(t);
                } else {
                    JOptionPane.showMessageDialog(null, "Ju lutem kontrolloni fieldat,shenoni parametrat e sakt , dhe mos leni fielda te pa plotesuar");
                }
            }
            loadTable();
        } catch (EmployeeException ex) {
            JOptionPane.showMessageDialog(this, "E dhena ekziton!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

